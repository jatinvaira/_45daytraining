public class bitwiseoperator {
    public static void main(String[] args) {
        int x = 1;
        int y = 2;

        

        System.out.println("x = " + x);
        System.out.println("y = " + y);
        // AND
        System.out.println("x&y " + (x & y));
        // OR
        System.out.println("x|y " + (x | y));
        // XOR
        System.out.println("x^y " + (x ^ y));

    }
}
